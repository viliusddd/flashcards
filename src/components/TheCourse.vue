<script setup lang="ts">
import {computed, ref} from 'vue'
import {fsrs, generatorParameters, Rating} from 'ts-fsrs'
import {useDeckStore} from '@/stores/DeckStore'
import {useRouter} from 'vue-router'
import type {Card} from '@/stores/DeckStore'

const router = useRouter()

const props = defineProps<{
  id: number
}>()

const deckStore = useDeckStore()

deckStore.deckId = props.id

const cardIndex = ref(0)
let cards: Card[]

const showAnswer = ref(false)

if (deckStore.dueReview) {
  cards = deckStore.cardsDue
  deckStore.dueReview = false
} else {
  cards = deckStore.cards
}

const card = computed(() => cards[cardIndex.value])

const progressBarValue = computed(() => {
  return (100 / deckStore.cards.length) * cardIndex.value
})

function goNextCard() {
  if (cardIndex.value === cards.length - 2) {
    cardIndex.value++
  } else if (cardIndex.value === cards.length - 1) {
    router.go(-1)
  } else {
    cardIndex.value++
  }
}

const fsrsParams = generatorParameters({enable_fuzz: true})
const fsrsInstance = fsrs(fsrsParams)

const schedulingCards = fsrsInstance.repeat(card.value, new Date())

// function saveCard(evt: PointerEvent) {
function saveCard(evt) {
  const btnVal = evt.target.innerText
  const btnRating = Rating[btnVal]
  const newCardVal = schedulingCards[btnRating].card
  deckStore.addCard(newCardVal)

  showAnswer.value = false

  goNextCard()
}

function markAsKnown() {
  card.value.state = 2
  goNextCard()
}
function markAsDifficult() {
  card.value.difficulty = 10
  goNextCard()
}
</script>

<template>
  <!-- <pre>{{ $route }}</pre> -->
  <div class="container">
    <header class="header">
      <div class="header__title">{{ deckStore?.deck?.name }}</div>
      <PButton
        class="header__tooltip"
        icon="pi pi-info-circle"
        severity="info"
        text
        rounded
        aria-label="Hotkeys info"
        v-tooltip.left="'Keyboard shortcuts'"
      />
      <PButton
        icon="pi pi-times"
        @click="$router.go(-1)"
        style="font-size: 4rem; margin: 0px"
        severity="info"
        text
        rounded
        aria-label="Cancel"
        v-tooltip.left="'Exit the session'"
      />
    </header>
    <div class="wrapper">
      <div class="container">
        <div class="flashcard">
          <ProgressBar
            class="flashcard__progress"
            :show-value="false"
            :value="progressBarValue"
          ></ProgressBar>
          <div
            class="flashcard__score"
            v-tooltip.left="'The position of current card in the deck.'"
          >
            {{ cardIndex + 1 }}
          </div>
          <div class="flashcard__body">
            <div class="flashcard__definition">
              <h1>{{ card.name }}</h1>
            </div>
            <div class="flashcard__answer">
              <button v-if="!showAnswer" @click="showAnswer = true">
                <p>show answer</p>
              </button>
              <div v-else class="flashcard__answer-revealed">
                <p>{{ card.definition }}</p>
              </div>
            </div>
          </div>
          <div class="flashcard__options">
            <PButton
              icon="pi pi-bolt"
              rounded
              text
              @click="markAsDifficult"
              v-tooltip.bottom="'Mark a card as difficult'"
            />
            <PButton
              icon="pi pi-ban"
              rounded
              text
              @click="markAsKnown"
              v-tooltip.bottom="
                'Mark this card as known and you will not be tested on it again'
              "
            />
          </div>
          <div class="difficulty__buttons">
            <PButton
              :raised="true"
              @click="saveCard"
              v-tooltip.bottom="'<10m'"
              label="Again"
            />
            <PButton
              :raised="true"
              @click="saveCard"
              v-tooltip.bottom="'2d'"
              label="Hard"
            />
            <PButton
              :raised="true"
              @click="saveCard"
              v-tooltip.bottom="'7d'"
              label="Good"
            />
            <PButton
              :raised="true"
              @click="saveCard"
              v-tooltip.bottom="'14d'"
              label="Easy"
            />
          </div>
        </div>
      </div>
    </div>
    <footer></footer>
  </div>
</template>

